import UIKit

class ViewController: UIViewController {

    @IBOutlet weak var btnCharacter: UIButton!
    @IBOutlet weak var btnComics: UIButton!
    @IBOutlet weak var btnCreators: UIButton!
    @IBOutlet weak var btnEvents: UIButton!
    @IBOutlet weak var btnSeries: UIButton!
    @IBOutlet weak var btnStories: UIButton!
    
    override func viewDidLoad() {
        super.viewDidLoad()
        let api: MarvelApi = MarvelApi()
        _ = api.getComic(id: 37500)
        
        btnCharacter.layer.cornerRadius = 10
        btnCharacter.clipsToBounds = true
        
        btnComics.layer.cornerRadius = 10
        btnComics.clipsToBounds = true
        
        btnCreators.layer.cornerRadius = 10
        btnCreators.clipsToBounds = true
        
        btnEvents.layer.cornerRadius = 10
        btnEvents.clipsToBounds = true
        
        btnSeries.layer.cornerRadius = 10
        btnSeries.clipsToBounds = true
        
        btnStories.layer.cornerRadius = 10
        btnStories.clipsToBounds = true
        //api.printComics()
    }
    
    override func didReceiveMemoryWarning() {
        super.didReceiveMemoryWarning()
        // Dispose of any resources that can be recreated.
    }

    @IBAction func viewCharacters(sender: UIButton) {
        let character:CharacterViewController = CharacterViewController()
        self.present(character, animated: true, completion: nil)
    }
    
    @IBAction func viewComics(sender: UIButton) {
        let comic:ComicViewController = ComicViewController()
        self.present(comic, animated: true, completion: nil)
    }
    
    @IBAction func viewCreators(sender: UIButton) {
        let creator: CreatorViewController = CreatorViewController()
        self.present(creator, animated: true, completion: nil)
    }
    
    @IBAction func viewEvents(sender: UIButton) {
        let event: EventViewController = EventViewController()
        self.present(event, animated: true, completion: nil)
    }
    
    @IBAction func viewSeries(sender: UIButton) {
        let series: SeriesViewController = SeriesViewController()
        self.present(series, animated: true, completion: nil)
    }
    
    @IBAction func viewStories(sender: UIButton) {
        let story: StoryViewController = StoryViewController()
        self.present(story, animated: true, completion: nil)
    }
}

